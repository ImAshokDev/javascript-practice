// Relational Operator

let x = 5;

console.log(x < 5);
console.log(x <= 5);

console.log(x > 5);
console.log(x >= 5);

// Equality Operator

console.log(x === 5);
console.log(x !== 5);

// string comparison
console.log("Alen" > "Anbu"); // dictionary

// comparison of different type

console.log("1" < 5); // 1<5
console.log(true == 1);

// 1 - True
// 0 - False
